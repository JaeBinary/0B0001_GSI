total = 0 #변수 선언 시 초기화는 필수!

total -= 900 * 10 #삼각김밥 (900원)10개 구입
total += 1800 * 2 #바나나맛 우유 (1800원)2개 판매
total -= 3500 * 5 #도시락(3500원) 5개 구입
total += 4000 * 4 #도시락(4000원) 4개 판매
total += 1500 * 1 #콜라(1500원) 1개 판매
total += 2000 * 4 #새우깡(2000원) 4개 판매
